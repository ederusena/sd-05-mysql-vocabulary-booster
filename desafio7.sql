SELECT UCASE(CONCAT(E.FIRST_NAME, ' ', E.LAST_NAME)) AS "Nome completo",
JH.START_DATE AS "Data de início",
E.SALARY AS "Salário"
FROM hr.job_history AS JH
INNER JOIN hr.employees AS E
ON JH.EMPLOYEE_ID = E.EMPLOYEE_ID AND MONTH(JH.START_DATE) BETWEEN 1 AND 3
ORDER BY CONCAT(E.FIRST_NAME, ' ', E.LAST_NAME), DATE(JH.START_DATE);
