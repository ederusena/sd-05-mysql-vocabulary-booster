SELECT J.JOB_TITLE AS "Cargo",
TRUNCATE((J.MAX_SALARY + J.MIN_SALARY) / 2, 2) AS "Média salarial",
CASE
WHEN ((J.MAX_SALARY + J.MIN_SALARY) / 2) BETWEEN 2000 AND 5800 THEN "Júnior"
WHEN ((J.MAX_SALARY + J.MIN_SALARY) / 2) BETWEEN 5801 AND 7500 THEN "Pleno"
WHEN ((J.MAX_SALARY + J.MIN_SALARY) / 2) BETWEEN 7501 AND 10500 THEN "Sênior"
WHEN ((J.MAX_SALARY + J.MIN_SALARY) / 2) > 10500 THEN "CEO"
END AS "Senioridade"
FROM hr.jobs AS J
ORDER BY ((J.MAX_SALARY + J.MIN_SALARY) / 2), J.JOB_TITLE;
