SELECT 
JB.JOB_TITLE AS 'Cargo', ROUND(AVG(SALARY), 2) AS 'Média salarial',
CASE
WHEN ROUND(AVG(SALARY), 2) BETWEEN 2000 AND 5800 THEN 'Júnior'
WHEN ROUND(AVG(SALARY), 2) BETWEEN 5801 AND 7500 THEN 'Pleno'
WHEN ROUND(AVG(SALARY), 2) BETWEEN 7501 AND 10500 THEN 'Sênior'
ELSE 'CEO'
END AS 'Senioridade'
FROM hr.employees AS EM
INNER JOIN hr.jobs AS JB
ON EM.JOB_ID = JB.JOB_ID
GROUP BY JB.JOB_TITLE
ORDER BY ROUND(AVG(SALARY), 2), JB.JOB_TITLE
